/**
 * Generates supabase/migrations/20260603000010_seed_players.sql
 * Source data: scripts/player-squads.mjs (projected WC 2026 squads / key players)
 */
import { writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { PLAYER_SQUADS } from "./player-squads.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, "../supabase/migrations/20260603000010_seed_players.sql");

function esc(s) {
  return s.replace(/'/g, "''");
}

const lines = [
  "-- Seed: player rosters for 48 WC 2026 group-phase teams",
  "-- Generated by: node scripts/generate-players-seed.mjs",
  "-- Approach: FIFA squads where available; otherwise key national-team players (2024–2026 cycle)",
  "BEGIN;",
  "DELETE FROM public.tournament_predictions WHERE top_scorer_player_id IS NOT NULL;",
  "DELETE FROM public.tournament_results WHERE top_scorer_player_id IS NOT NULL;",
  "DELETE FROM public.players WHERE id_team IN (",
  "  SELECT id_team FROM public.teams WHERE id_group IS NOT NULL",
  ");",
  "",
];

let totalPlayers = 0;
const teamCounts = [];

for (const [code, players] of Object.entries(PLAYER_SQUADS)) {
  teamCounts.push({ code, count: players.length });
  totalPlayers += players.length;

  const valueRows = players
    .map(
      ([name, position, club, clubCountry]) =>
        `  ('${esc(name)}', '${esc(position)}', '${esc(club)}', '${esc(clubCountry)}')`,
    )
    .join(",\n");

  lines.push(
    `INSERT INTO public.players (id_team, name, position, club, club_country)`,
    `SELECT t.id_team, p.name, p.position, p.club, p.club_country`,
    `FROM public.teams t`,
    `CROSS JOIN (`,
    `  VALUES`,
    valueRows,
    `) AS p(name, position, club, club_country)`,
    `WHERE t.code = '${code}';`,
    "",
  );
}

lines.push("INSERT INTO public.tournament_results (id) VALUES (1) ON CONFLICT (id) DO NOTHING;");
lines.push("COMMIT;");
lines.push("");
lines.push(`-- Total: ${totalPlayers} players across ${teamCounts.length} teams`);

writeFileSync(outPath, lines.join("\n"), "utf8");

console.log(`Wrote ${outPath} (${lines.join("\n").length} bytes)`);
console.log(`Teams: ${teamCounts.length}, Players: ${totalPlayers}`);
